using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.Extensions.Configuration;
using GeoMapping.Interfaces;
using GeoMapping.Models;
using GeoMapping.DTO;
using GeoMapping.Context;
using Microsoft.EntityFrameworkCore;

namespace GeoMapping.Repositories
{
    public class GeoMappingRepository : IGeoAddressRepository
    {
        private GeoMappingContext _context = null;
        private DbSet<Address> _addresses = null;
        private readonly IConfiguration _configuration;


        public GeoMappingRepository()
        {
            this._context = new GeoMappingContext();
            this._addresses= _context.Set<Address>();
        
        }


        public async Task<List<Address>> GetAddressesList()
        {
            var list = new List<Address>();

            try
            {


                list = await _addresses.ToListAsync();

            }
            catch (Exception ex)
            {
                throw ex;
            }

            return list;

        }


        public async Task<Address> AddAddress(Address _obj)
        {
            try
            {
                _context.Addresses.AddAsync(_obj);
                await _context.SaveChangesAsync();

            }
            catch (Exception ex)
            {
                throw ex;
            }
            return _obj;
        }
 



    }
}
































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































